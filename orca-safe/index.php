					As Southern Resident Killer Whale recovery has become a regional passion and a national priority, and as Chinook availability for Orcas has emerged as a key driver in Orca recovery, our customer base has demanded to know that Lummi Island Wild products are produced in harmony with whale recovery efforts and goal.  
					We have closely examined our buying practices and supply-chain process to ensure that we can continue to support our fisher-partners and supply our quality and values-driven customer base.
				</p>
				<p>
					The Orca Safe Salmon program is our answer to this question.
					Orca Safe is a Lummi Island Wild trade claim asserting that we have determined, to our satisfaction, that a given fishery is conducted in such a way as to not compete with Orcas for food resource and in such a way as to foster long-term wild salmon restoration and Orca recovery.  
					Our process and supply chain are completely transparent and we are currently working on populating the OrcaSafe.org site with partnership endorsements, links and data supporting our Orca Safe claim.
				</p>
