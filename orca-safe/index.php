				<h2>
					Good Science and Healthy Eating
				</h2>
 				<p>
					Conscious consumers can be reassured by the Orca Safe Salmon seal that their purchasing and dining habits are in sync with their values. 
					Many Chinook are harvested using sustainable methods which are purposefully designed to have no impact on orca food availability. 				
				</p>
				<p>
 					Tribal fishing communities are the primary beneficiaries of commercial Chinook salmon sales, and they have are the greatest advocates for protecting and restoring this resource. 
					Good science, healthy eating and support of tribal fisheries all come together in the Orca Safe Salmon label on king salmon.
				</p>
