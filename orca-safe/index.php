				<h1>
					Introducing Orca Safe Salmon
				</h1>
				<p>
					<span>Orca Safe Salmon means</span> the fish you are eating has been caught in a terminal area, meaning in or close to the salmon's river of origin, well past the point that a fish might make a meal for an Orca. 
				</p>
				<p>
					<span>Orca Safe Salmon means</span> that the fish you are eating comes from a healthy returning population and is surplus to the spawning needs of the run. 
				</p>
				<p>
					<span>Orca Safe Salmon means</span> the fish you are eating has been caught by sustainable gear with little or no by-catch, in a fishery carefully controlled by Tribal, Federal and State fisheries managers.	
				</p>
